export function sumaDeTresNumeros( a: number, b: number, c: number): number {
    return a + b + c;
}

export function restaDeTresNumeros( a: number, b: number, c: number): number {
    return a - b - c;
}

export function multiplicacionTresNumeros( a: number, b: number, c: number): number {
    return a * b * c;
}